// Get featured lessons
export const getFeaturedLessons = () => {
  return lessons.filter(lesson => lesson.isFeatured);
};

// Get lessons by category
export const getLessonsByCategory = (category) => {
  return lessons.filter(lesson => lesson.category === category);
};

// Get lessons by emotional tone
export const getLessonsByTone = (tone) => {
  return lessons.filter(lesson => lesson.emotionalTone === tone);
};

// Get most saved lessons
export const getMostSavedLessons = (limit = 6) => {
  return [...lessons]
    .sort((a, b) => b.favoritesCount - a.favoritesCount)
    .slice(0, limit);
};

// Get lessons by user
export const getLessonsByUser = (email) => {
  return lessons.filter(lesson => lesson.creatorEmail === email);
};

